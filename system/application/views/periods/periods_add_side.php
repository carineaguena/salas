<dl>
  <dt>Time format</dt>
  <dd>Os formatos suportados para o horário de início e horário de fim são <span>HHMM</span> ou <span>HH:MM</span>. Por exemplo, <em>0930</em> ou <em>14:20</em>. Você deve incluir o 0 se estiver usando o formato HHMM, como no exemplo.</dd>
	<dd>Todos os horários devem ser inseridos no formato <span>24 horas</span> entre 00:00 e 23:59.</dd>
</dl>
